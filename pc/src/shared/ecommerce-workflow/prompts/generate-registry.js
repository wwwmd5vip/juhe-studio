const fs = require('node:fs')
const path = require('node:path')

const sourceDir = path.resolve(__dirname, '../../../../提示词/智能体')
const outputFile = path.resolve(__dirname, 'agent-prompts.ts')

const files = fs
  .readdirSync(sourceDir)
  .filter((f) => f.endsWith('.md'))
  .sort()

function quoteKey(key) {
  // 符合标识符规则时省略引号，与 Biome 格式化风格保持一致
  if (/^[$_\p{ID_Start}][$\p{ID_Continue}]*$/u.test(key)) {
    return key
  }
  return `'${key.replace(/\\/g, '\\\\').replace(/'/g, "\\'")}'`
}

function quoteString(str) {
  return `'${str.replace(/\\/g, '\\\\').replace(/'/g, "\\'")}'`
}

const imports = []
const entries = []

for (const [index, file] of files.entries()) {
  const key = file.replace(/\.md$/i, '')
  const exportName = `prompt_${index.toString().padStart(3, '0')}`
  const content = fs.readFileSync(path.join(sourceDir, file), 'utf-8')
  const escaped = content.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$/g, '\\$')
  imports.push(`const ${exportName} = \`${escaped}\``)
  entries.push(
    `  ${quoteKey(key)}: {\n    id: ${quoteString(key)},\n    name: ${quoteString(key)},\n    prompt: ${exportName}\n  }`
  )
}

const output = `// AUTO-GENERATED by generate-registry.js
// Source: 提示词/智能体/*.md
// Do not edit manually; run \`node src/shared/ecommerce-workflow/prompts/generate-registry.js\` to regenerate.

import type { AgentPrompt } from '../types'

${imports.join('\n\n')}

export const AGENT_PROMPTS: Record<string, AgentPrompt> = {
${entries.join(',\n')}
}
`

fs.writeFileSync(outputFile, output, 'utf-8')
console.log(`Generated ${files.length} agent prompts -> ${outputFile}`)
